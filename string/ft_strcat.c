char	*ft_strcat(char *dest, char *src)
{
	int i;
	int is;

	i = 0;
	is = 0;
	while (dest[i])
		i++;
	while (src[is])
		dest[i++] = src[is++];
	dest[i] = '\0';
	return (dest);
}
