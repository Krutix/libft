int	ft_sqrt(int nb)
{
	unsigned int i;
	unsigned int unb;

	i = 0;
	if (nb < 0)
		return (0);
	unb = nb;
	while (i * i <= unb)
	{
		if (i * i == unb)
			return (i);
		i++;
	}
	return (0);
}
